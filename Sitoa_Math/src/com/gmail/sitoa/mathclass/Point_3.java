package com.gmail.sitoa.mathclass;

public class Point_3 {
	public double x;
	public double y;
	public double z;
	
	public Point_3(double a,double b,double c){
		x=a;
		y=b;
		z=c;
		
	}
	

	public double getx(){
		return x;
	}
	public double gety(){
		return y;
	}
	public double getz(){
		return z;
	}

}
